            <h3>Pengolahan tanah</h3>
            <p>Lahan yang diperlukan untuk budidaya cabe merah adalah tanah yang gembur dan memiliki porosotas yang baik. Sebelum cabe merah ditanam cangkul atau bajak lahan sedalam 20-40 cm. Bersihkan dari batu atau kerikil dan sisa-sisa akar tanaman. Apabila terlalu banyak gulma dan khawatir menganggu bisa gunakan herbisida.</p>
            <p>Buat bedengan dengan lebar satu meter tinggi 30-40 cm dan jarak antar bedengan 60 cm. Panjang bedengan disesuaikan dengan kondisi lahan, untuk memudahkan pemeliharaan panjang bedengan maksimal 15 meter. Buat saluran drainase yang baik karena tanaman cabe merah tidak tahan terhadap genangan air.</p>
          <p>Budidaya cabe merah menghendaki tanah yang memiliki tingkat keasaman tanah pH 6-7. Apabila nilainya terlalu rendah (asam), daun tanaman cabe merah akan terlihat pucat dan mudah terserang virus. Tanah yang asam biasanya mudah ditumbuhi ilalang. Untuk menetralisirnya bisa gunakan kapur pertanian atau dolomit sebanyak 2-4 ton/ha. Pemberian kapur atau dolomit dilakukan pada saat pembajakan dan pembuatan bedengan.</p>
        <p>Campurkan pupuk organik, bisa berupa kompos atau pupuk kandang pada setiap bedengan secara merata. Kebutuhan pupuk organik untuk budidaya cabe merah adalah 20 ton per hektar. Selain pupuk organik tambahkan juga urea 350 kg/ha dan KCl 200kg/ha.
Untuk budidaya cabe intensif sebaiknya, bedengan ditutup dengan mulsa plastik perak hitam. Penggunaan mulsa plastik mempunyai konsekuensi biaya namun mendatangkan sejumlah manfaat. Mulsa bermanfaat untuk mempertahankan kelembaban, menekan erosi, mengendalikan gulma dan menjaga kebersihan kebun.</p>  
<p>Buat lubang tanam sebanyak dua baris dalam setiap bedengan dengan jarak 60-70 cm. Sebaiknya lubang tanam dibuat zig zag, tidak sejajar. Hal ini berguna untuk mengatur sirkulasi angin dan penetrasi sinar matahari. Diameter dan kedalaman lubang tanam kurang lebih 10 cm, atau disesuaikan dengan ukuran polybag semai.</p>



